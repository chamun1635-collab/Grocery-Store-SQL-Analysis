 5 Employee Performance
 
1. How many employees processed orders?
SELECT COUNT(DISTINCT emp_id) AS active_employees
FROM orders;

2. Employees who handled most orders
SELECT e.emp_name, COUNT(o.ord_id) AS total_orders
FROM employees e
JOIN orders o ON e.emp_id = o.emp_id
GROUP BY e.emp_name
ORDER BY total_orders DESC;

3. Total sales handled by each employee
SELECT e.emp_name, SUM(od.total_price) AS total_sales
FROM employees e
JOIN orders o ON e.emp_id = o.emp_id
JOIN order_details od ON o.ord_id = od.ord_id
GROUP BY e.emp_name
ORDER BY total_sales DESC;


4. Average order value per employee
SELECT e.emp_name, AVG(order_total) AS avg_order_value
FROM employees e
JOIN (
    SELECT o.emp_id, o.ord_id, SUM(od.total_price) AS order_total
    FROM orders o
    JOIN order_details od ON o.ord_id = od.ord_id
    GROUP BY o.emp_id, o.ord_id
) AS sub
ON e.emp_id = sub.emp_id
GROUP BY e.emp_name;


