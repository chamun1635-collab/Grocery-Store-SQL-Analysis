c.cust_name,
SUM(od.total_price) AS total_spent
FROM customers c
JOIN orders o ON c.cust_id = o.cust_id
JOIN order_details od ON o.ord_id = od.ord_id
GROUP BY c.cust_name
ORDER BY total_spent DESC
LIMIT 5;


Product Performance

1. How many products exist in each category?
SELECT c.cat_name, COUNT(p.prod_id) AS total_products
FROM categories c
JOIN products p ON c.cat_id = p.cat_id
GROUP BY c.cat_name;

2. Average price of products by category
SELECT c.cat_name, AVG(p.price) AS avg_price
FROM categories c
JOIN products p ON c.cat_id = p.cat_id
GROUP BY c.cat_name;

3. Products with highest sales (by quantity)
SELECT p.prod_name, SUM(od.quantity) AS total_quantity
FROM products p
JOIN order_details od ON p.prod_id = od.prod_id
GROUP BY p.prod_name
ORDER BY total_quantity DESC;

4. Total revenue generated by each product
SELECT p.prod_name, SUM(od.total_price) AS total_revenue
FROM products p
JOIN order_details od ON p.prod_id = od.prod_id
GROUP BY p.prod_name
ORDER BY total_revenue DESC;


5. Product sales by category and supplier
SELECT c.cat_name, s.sup_name, SUM(od.total_price) AS total_sales
FROM order_details od
JOIN products p ON od.prod_id = p.prod_id
JOIN categories c ON p.cat_id = c.cat_id
JOIN supplier s ON p.sup_id = s.sup_id
GROUP BY c.cat_name, s.sup_name
ORDER BY total_sales DESC;


3 sales and Order Trends

1. Total number of orders
SELECT COUNT(*) AS total_orders
FROM orders;

2. Average value per order
SELECT AVG(order_total) AS avg_order_value
FROM (
    SELECT ord_id, SUM(total_price) AS order_total
    FROM order_details
    GROUP BY ord_id
) AS sub;


3. Dates with highest number of orders
SELECT order_date, COUNT(ord_id) AS total_orders
FROM orders
GROUP BY order_date
ORDER BY total_orders DESC;

4. Monthly trends in orders and revenue
SELECT 
MONTH(order_date) AS month,
COUNT(o.ord_id) AS total_orders,
SUM(od.total_price) AS total_revenue
FROM orders o
JOIN order_details od ON o.ord_id = od.ord_id
GROUP BY MONTH(order_date)
ORDER BY month;

5. Weekday vs Weekend orders
SELECT 
CASE 
    WHEN DAYOFWEEK(order_date) IN (1,7) THEN 'Weekend'
    ELSE 'Weekday'
END AS day_type,
COUNT(*) AS total_orders
FROM orders
GROUP BY day_type;


